package Handson1;

public class ForeachLoop {
    public static void main(String[] args) {
        String[] buah = {"Apel", "Pisang", "Mangga", "Jeruk"};

        System.out.println("Menampilkan semua elemen buah:");
        for (String b : buah){
            System.out.println(b);
        }
    }
}
