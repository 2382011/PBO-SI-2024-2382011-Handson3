package Handson1;

public class ForLoop {
    public static void main(String[] args) {
                String[] buah = {"Apel", "Pisang", "Mangga", "Jeruk"};

                System.out.println("Menampilkan semua elemen buah:");
                for (int i = 0; i < buah.length; i++){
                    System.out.println(buah[i]);
                }

            }
        }